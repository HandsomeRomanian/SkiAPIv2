CREATE TRIGGER `trg_DnbStudents` AFTER DELETE ON `StudentGroup` FOR EACH ROW UPDATE
    `Group` as G
    INNER JOIN (
        SELECT
            count(StudentID) as 'count',
            GroupID
        FROM
            `StudentGroup` SG
        WHERE
            SG.GroupID = GroupID
        GROUP BY
            GroupID
    ) as A on G.GroupID = A.GroupID
SET
    G.nbStudents = A.count
$$
DELIMITER ;
